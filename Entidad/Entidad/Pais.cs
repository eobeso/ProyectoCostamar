namespace Entidad
{
    public class Pais
    {
        private int codPais;
        private string descripcion;

        public int CodPais
        {
            get
            {
                return codPais;
            }

            set
            {
                codPais = value;
            }
        }
        public string Descripcion
        {
            get
            {
                return descripcion;
            }

            set
            {
                descripcion = value;
            }
        }
    }
}
